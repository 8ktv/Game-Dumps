	private static byte SBox(byte x)
	{
		return S[x >>> 4, x & 0xF];
	}

	private static byte[] SubBytes(byte[] s)
	{
		byte[] array = new byte[s.Length];
		for (int i = 0; i < 16; i++)
		{
			array[i] = SBox(s[i]);
		}
		return array;
	}

